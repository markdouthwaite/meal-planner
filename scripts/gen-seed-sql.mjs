// Generates supabase/seed_recipes.sql from the curated SEED_RECIPES in
// src/utils/seedData.ts. Base recipes are inserted as global rows
// (household_id IS NULL) and the script is idempotent (skips titles already
// present), so it's safe to re-run.
//
//   node scripts/gen-seed-sql.mjs
//
// Re-run whenever the starter recipes change, then apply the SQL in the
// Supabase SQL editor (or via the CLI).

import { build } from 'esbuild';
import { writeFileSync, rmSync } from 'fs';
import { pathToFileURL } from 'url';
import { resolve } from 'path';

const tmp = resolve('tmp-seed.mjs');

await build({
  entryPoints: ['src/utils/seedData.ts'],
  bundle: true,
  format: 'esm',
  platform: 'node',
  outfile: tmp,
  logLevel: 'silent',
});

const { SEED_RECIPES } = await import(pathToFileURL(tmp).href);
rmSync(tmp);

/** Single-quoted SQL string literal with quotes escaped. */
const q = s => `'${String(s).replace(/'/g, "''")}'`;
/** Postgres text[] literal from a JS string array. */
const arr = (a) => (a.length ? `array[${a.map(q).join(', ')}]::text[]` : `'{}'::text[]`);
/** jsonb literal from a JS value. */
const json = (v) => `${q(JSON.stringify(v))}::jsonb`;

const statements = SEED_RECIPES.map((r) => {
  const image = r.image == null ? 'null' : q(r.image);
  return `insert into recipes
  (household_id, title, description, image, ingredients, steps, meal_type, servings, notes, source_url, tags)
select null, ${q(r.title)}, ${q(r.description)}, ${image}, ${json(r.ingredients)},
       ${arr(r.steps)}, ${arr(r.meal_type)}, ${r.servings}, ${q(r.notes)}, ${q(r.source_url)}, ${arr(r.tags)}
where not exists (
  select 1 from recipes where household_id is null and title = ${q(r.title)}
);`;
});

const header = `-- Base (global) recipes — shared read-only by every household.
--
-- GENERATED by scripts/gen-seed-sql.mjs from src/utils/seedData.ts. Do not edit
-- by hand; re-run the generator instead.
--
-- Apply manually in the Supabase SQL editor (or via the CLI). Idempotent: each
-- statement skips a recipe whose title is already a base recipe, so re-running
-- is safe. Requires migration 0002_shared_recipes.sql (nullable household_id).

`;

const footer = `

-- ---------------------------------------------------------------------------
-- OPTIONAL one-time cleanup (run only if needed)
-- ---------------------------------------------------------------------------
-- Households created before base recipes existed were seeded with their OWN
-- copies of these starter recipes. Now that the same recipes exist globally,
-- those copies are duplicates. The statement below removes the *untouched*
-- copies (never edited: updated_at = created_at) whose title matches a base
-- recipe. Any plan slots pointing at them are set to NULL (ON DELETE SET NULL).
-- Review before running; comment out if your household has customised them.
--
-- delete from recipes c
-- where c.household_id is not null
--   and c.updated_at = c.created_at
--   and exists (
--     select 1 from recipes b
--     where b.household_id is null and b.title = c.title
--   );
`;

writeFileSync('supabase/seed_recipes.sql', header + statements.join('\n\n') + footer + '\n');
console.log(`Wrote supabase/seed_recipes.sql (${SEED_RECIPES.length} base recipes).`);
